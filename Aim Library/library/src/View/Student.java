    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         int selectedIndex = jTable1.getSelectedRow();
    if (selectedIndex != -1) {
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        txtStudentID.setText(DFT.getValueAt(selectedIndex, 0).toString());
        txtname.setText(DFT.getValueAt(selectedIndex, 1).toString());
        txtaddress.setText(DFT.getValueAt(selectedIndex, 2).toString());
        txtphone.setText(DFT.getValueAt(selectedIndex, 3).toString());
    }
    }//GEN-LAST:event_jTable1MouseClicked

    
